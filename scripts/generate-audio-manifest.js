#!/usr/bin/env node
/**
 * Generates a TypeScript manifest mapping audio paths to require() calls.
 * Metro statically analyzes require() so each must be a literal string.
 *
 * Usage: node scripts/generate-audio-manifest.js
 */

const fs = require('fs');
const path = require('path');

const PUBLIC_DIR = path.join(__dirname, '..', 'public', 'audio');
const OUTPUT_FILE = path.join(__dirname, '..', 'data', 'audioManifest.generated.ts');

function walkDir(dir, base) {
  let results = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      results = results.concat(walkDir(fullPath, base));
    } else if (entry.name.endsWith('.mp3')) {
      const relativePath = '/' + path.relative(base, fullPath).split(path.sep).join('/');
      results.push(relativePath);
    }
  }
  return results;
}

// Scan public/audio/ for all mp3 files
const publicBase = path.join(__dirname, '..', 'public');
const audioPaths = walkDir(PUBLIC_DIR, publicBase).sort();

console.log(`Found ${audioPaths.length} audio files`);

// Generate the manifest with literal require() calls
const lines = audioPaths.map((p) => {
  // require path is relative from data/ to public/
  const requirePath = `../public${p}`;
  return `  '${p}': require('${requirePath}'),`;
});

const output = `// Auto-generated by scripts/generate-audio-manifest.js
// Do not edit manually. Regenerate with: node scripts/generate-audio-manifest.js
/* eslint-disable */

const manifest: Record<string, number> = {
${lines.join('\n')}
};

export default manifest;
`;

fs.writeFileSync(OUTPUT_FILE, output);
console.log(`Wrote manifest to ${path.relative(process.cwd(), OUTPUT_FILE)}`);
